package com.sunrun.entity;


/**
 *  菜单表信息
 * @author jl
 *
 */
public class MenuBean   { 
	 
	String functionsId ;
	String modeId;
	String functionsModeNumber;
	String functionsTableName;
	String title;
	String badge;
	public String getFunctionsId() {
		return functionsId;
	}
	public void setFunctionsId(String functionsId) {
		this.functionsId = functionsId;
	}
	public String getModeId() {
		return modeId;
	}
	public void setModeId(String modeId) {
		this.modeId = modeId;
	}
	public String getFunctionsModeNumber() {
		return functionsModeNumber;
	}
	public void setFunctionsModeNumber(String functionsModeNumber) {
		this.functionsModeNumber = functionsModeNumber;
	}
	public String getFunctionsTableName() {
		return functionsTableName;
	}
	public void setFunctionsTableName(String functionsTableName) {
		this.functionsTableName = functionsTableName;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBadge() {
		return badge;
	}
	public void setBadge(String badge) {
		this.badge = badge;
	}
	
	
	
 	
}
